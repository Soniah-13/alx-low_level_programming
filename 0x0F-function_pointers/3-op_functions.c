#include <stdlib.h>
/**
 * op_add - add function
 * @a: input one
 * @b: input two
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtraction function
 * @a: input one
 * @b: input two
 * Return: defference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul -Multiplication
 * @a: input one
 * @b: input two
 * Return: Product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Division
 * @a: input one
 * @b: input two
 * Return: Divided numbers
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod -  Modulas
 * @a: input one
 * @b: input two
 * Return: Remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
